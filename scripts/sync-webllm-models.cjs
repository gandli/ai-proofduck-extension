const fs = require('node:fs/promises');
const path = require('node:path');
const { execFileSync } = require('node:child_process');

const OUTPUT_PATH = path.join(process.cwd(), 'lib', 'processing', 'official-webllm-models.ts');

function readContextWindow(item) {
  if (typeof item?.overrides?.context_window_size === 'number') {
    return item.overrides.context_window_size;
  }

  if (typeof item?.context_window_size === 'number') {
    return item.context_window_size;
  }

  return null;
}

async function main() {
  const rawJson = execFileSync(
    process.execPath,
    [
      '-e',
      `
        import('@mlc-ai/web-llm')
          .then((module) => {
            const rawList = module.prebuiltAppConfig?.model_list ?? [];
            process.stdout.write(JSON.stringify(rawList));
          })
          .catch((error) => {
            console.error(error);
            process.exit(1);
          });
      `,
    ],
    {
      cwd: process.cwd(),
      encoding: 'utf8',
    },
  );

  const rawList = JSON.parse(rawJson);

  const next = rawList
    .map((item) => ({
      id: item.model_id,
      vram: typeof item.vram_required_MB === 'number' ? item.vram_required_MB : null,
      context: readContextWindow(item),
    }))
    .sort((a, b) => {
      const byVram = (a.vram ?? Number.MAX_SAFE_INTEGER) - (b.vram ?? Number.MAX_SAFE_INTEGER);
      if (byVram !== 0) return byVram;
      return a.id.localeCompare(b.id);
    });

  const fileContent = `// Generated by scripts/sync-webllm-models.cjs from @mlc-ai/web-llm.\n` +
    `// Run \`bun run models:sync\` after upgrading @mlc-ai/web-llm.\n\n` +
    `export interface OfficialLocalModelOption {\n` +
    `  id: string;\n` +
    `  vram: number | null;\n` +
    `  context: number | null;\n` +
    `}\n\n` +
    `export const OFFICIAL_LOCAL_MODELS: OfficialLocalModelOption[] = ${JSON.stringify(next, null, 2)};\n`;

  await fs.writeFile(OUTPUT_PATH, fileContent, 'utf8');
  console.log(`Synced ${next.length} official WebLLM models to ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
